/* ───────────────────────────────────────────── */
/* Internal Helpers                              */
/* ───────────────────────────────────────────── */

static bool add_range(np_port_spec_t *spec, uint16_t start, uint16_t end)
{
    if (!spec)
        return false;

    if (spec->count >= NP_MAX_PORT_RANGES)
        return false;

    if (start < NP_PORT_MIN || end > NP_PORT_MAX || start > end)
        return false;

    spec->ranges[spec->count++] = (np_port_range_t){ start, end };
    return true;
}

/* ───────────────────────────────────────────── */
/* Port Parsing                                 */
/* ───────────────────────────────────────────── */

bool np_parse_ports(const char *spec, np_port_spec_t *out)
{
    if (!spec || !out)
        return false;

    memset(out, 0, sizeof(*out));

    /* Full range: - */
    if (strcmp(spec, "-") == 0)
    {
        return add_range(out, NP_PORT_MIN, NP_PORT_MAX);
    }

    char *copy = strdup(spec);
    if (!copy)
        return false;

    char *tok = strtok(copy, ",");
    while (tok)
    {
        char *dash = strchr(tok, '-');
        if (dash)
        {
            *dash = '\0';
            uint16_t start = (uint16_t)atoi(tok);
            uint16_t end   = (uint16_t)atoi(dash + 1);

            if (!add_range(out, start, end))
                goto fail;
        }
        else
        {
            uint16_t port = (uint16_t)atoi(tok);
            if (!add_range(out, port, port))
                goto fail;
        }

        tok = strtok(NULL, ",");
    }

    free(copy);
    return out->count > 0;

fail:
    free(copy);
    return false;
}

/* ───────────────────────────────────────────── */
/* Port Count                                   */
/* ───────────────────────────────────────────── */

uint64_t np_ports_total(const np_port_spec_t *spec)
{
    if (!spec)
        return 0;

    uint64_t total = 0;
    for (uint32_t i = 0; i < spec->count; i++)
    {
        total += (uint64_t)(spec->ranges[i].end -
                            spec->ranges[i].start + 1);
    }
    return total;
}

/* ───────────────────────────────────────────── */
/* ✅ SAFE PORT ITERATOR                         */
/* ───────────────────────────────────────────── */

void np_port_iter_init(np_port_iter_t *it)
{
    memset(it, 0, sizeof(*it));
}

bool np_port_iter_next(const np_port_spec_t *spec,
                       np_port_iter_t *it,
                       uint16_t *out_port)
{
    if (!spec || !it || !out_port)
        return false;

    if (it->range_idx >= spec->count)
        return false;

    np_port_range_t *r = &spec->ranges[it->range_idx];

    if (!it->started)
    {
        it->port = r->start;
        it->started = true;
    }
    else
    {
        it->port++;
    }

    if (it->port > r->end)
    {
        it->range_idx++;
        it->started = false;
        return np_port_iter_next(spec, it, out_port);
    }

    *out_port = it->port;
    return true;
}