uint16_t np_ports_get(const np_port_spec_t *spec, uint64_t index)
{
    if (!spec || spec->count == 0)
        return 0;

    uint64_t base = 0;

    for (uint32_t i = 0; i < spec->count; i++)
    {
        uint16_t start = spec->ranges[i].start;
        uint16_t end = spec->ranges[i].end;

        if (start == 0 || end == 0 || end < start)
            continue;

        uint64_t range_len = (uint64_t)end - (uint64_t)start + 1;

        if (index < base + range_len)
        {
            return (uint16_t)(start + (index - base));
        }

        base += range_len;
    }

    return 0; /* index out of bounds */
}

